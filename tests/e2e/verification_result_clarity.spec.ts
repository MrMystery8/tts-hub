import { expect, test } from '@playwright/test';

function silentWav(): Buffer {
  const samples = 800;
  const dataSize = samples * 2;
  const buffer = Buffer.alloc(44 + dataSize);
  buffer.write('RIFF', 0);
  buffer.writeUInt32LE(36 + dataSize, 4);
  buffer.write('WAVEfmt ', 8);
  buffer.writeUInt32LE(16, 16);
  buffer.writeUInt16LE(1, 20);
  buffer.writeUInt16LE(1, 22);
  buffer.writeUInt32LE(8000, 24);
  buffer.writeUInt32LE(16000, 28);
  buffer.writeUInt16LE(2, 32);
  buffer.writeUInt16LE(16, 34);
  buffer.write('data', 36);
  buffer.writeUInt32LE(dataSize, 40);
  return buffer;
}

test.describe('plain-language verification results', () => {
  test.use({ viewport: { width: 1440, height: 900 } });

  test('leads with a verdict and keeps desktop detector evidence collapsed', async ({ page }) => {
    const now = Date.now() / 1000;
    const model = { id: 'index-tts2', name: 'IndexTTS2', description: 'Index' };
    const run = { id: 'run-a', label: 'Primary detector', status: 'completed', updated_at: now };
    let detectionCount = 0;

    await page.route('**/api/models', route => route.fulfill({ contentType: 'application/json', body: JSON.stringify({ models: [model] }) }));
    await page.route('**/api/info', route => route.fulfill({ contentType: 'application/json', body: JSON.stringify({ time: now, ffmpeg: { available: true } }) }));
    await page.route('**/api/status', route => route.fulfill({ contentType: 'application/json', body: JSON.stringify({ models: { [model.id]: { loaded: true, device: 'test', total_generations: 0 } } }) }));
    await page.route('**/api/voices', route => route.fulfill({ contentType: 'application/json', body: JSON.stringify({ voices: [] }) }));
    await page.route('**/api/generation-jobs', route => route.fulfill({ contentType: 'application/json', body: JSON.stringify({ jobs: [] }) }));
    await page.route('**/api/watermark/runs', route => route.fulfill({ contentType: 'application/json', body: JSON.stringify({ default_run_id: run.id, runs: [run] }) }));
    await page.route('**/api/watermark/run_details**', route => route.fulfill({ contentType: 'application/json', body: JSON.stringify({ ...run, metrics: { auc: 0.988 }, config: { threshold: 0.35 } }) }));
    await page.route('**/api/watermark/detect', route => {
      detectionCount += 1;
      const detected = detectionCount === 1;
      return route.fulfill({
        contentType: 'application/json',
        body: JSON.stringify({
          detected,
          wm_prob: detected ? 0.913 : 0.05,
          model: detected ? { id: 0, name: 'IndexTTS2', tts_model_id: model.id } : null,
          run: { id: run.id },
        }),
      });
    });

    await page.goto('/');
    await page.locator('[data-tour="nav"] button').filter({ hasText: 'Watermark' }).click();

    await expect(page.getByText('View detector run details', { exact: true })).toBeVisible();
    await expect(page.getByText('Loading run details...')).toBeHidden();

    const chooseAudio = async (name: string) => {
      const fileChooserPromise = page.waitForEvent('filechooser');
      await page.getByRole('button', { name: 'Choose audio' }).click();
      const fileChooser = await fileChooserPromise;
      await fileChooser.setFiles({ name, mimeType: 'audio/wav', buffer: silentWav() });
    };

    await chooseAudio('watermarked.wav');
    await expect(page.getByRole('status')).toContainText('Watermark detected');
    await expect(page.getByRole('status')).toContainText('Likely generated by IndexTTS2');
    await expect(page.getByRole('status')).not.toContainText('Clear result');
    await expect(page.getByRole('status').getByText('Raw score')).toBeHidden();

    await page.getByRole('status').getByText('View technical details', { exact: true }).click();
    await expect(page.getByRole('status').getByText('Raw score')).toBeVisible();
    await expect(page.getByRole('status')).toContainText('0.913');

    await chooseAudio('clean.wav');
    await expect(page.getByRole('status')).toContainText('No watermark detected');
    await expect(page.getByRole('status')).toContainText('No embedded TTS Hub watermark was found');
    await expect(page.getByRole('status')).not.toContainText('unknown source model');
    await expect(page.getByRole('status').getByText('Raw score')).toBeHidden();
    await page.getByRole('status').getByText('View technical details', { exact: true }).click();
    await expect(page.getByRole('status')).not.toContainText('Source model');
  });
});
